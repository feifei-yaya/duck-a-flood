CITY_COUNTIES = {
    'Atlanta': 'Fulton',
    'Savannah': 'Chatham',
    'Augusta': 'Richmond',
    'Columbus': 'Muscogee',
    'St. Simons': 'Glynn',
    'Macon': 'Bibb',
    'Albany': 'Dougherty',
    'Athens': 'Clarke',
    'Roswell': 'Fulton'
}
import requests

def get_nws_alert_level(city_name):
    # returns nummerical severeness (0-1.0 scale) of severity of alert 
    county = CITY_COUNTIES.get(city_name, '')
    
    try:
        response = requests.get(
            "https://api.weather.gov/alerts/active?area=GA", #api with live updates
            timeout=5,
            headers={"User-Agent": "CrisisMap/1.0"}  # NWS requires a User-Agent
        )
        data = response.json()
    except Exception:
        return 0.0  # API down, return safe fallback, also sometimes no risk 

    best_score = 0.0
    
    for feature in data.get("features", []):
        props = feature.get("properties", {})
        event = props.get("event", "")
        area  = props.get("areaDesc", "")
        
        # Check if this alert covers our city's county
        if county.lower() not in area.lower():
            continue
        
        # Score the alert type
        if "Flood Warning" in event or "Flash Flood Emergency" in event:
            best_score = max(best_score, 1.0)
        elif "Flood Watch" in event or "Flash Flood Watch" in event:
            best_score = max(best_score, 0.67)
        elif "Flood Advisory" in event:
            best_score = max(best_score, 0.33)
    
    return best_score #if no risk / another warning then just returns 0 

