def get_gauge_ratio(city_name):
    # Map cities to nearby USGS gauge station IDs
    # IDs generated by perplexity based on source 
    GAUGE_STATIONS = {
    'Atlanta': '02336000',     # Chattahoochee River at Atlanta, GA [web:25]
    'Savannah': '021989773',   # Savannah River at USACE Dock, at Savannah, GA [web:21]
    'Augusta': '02197000',     # Savannah River at Augusta, GA [web:26]
    'Columbus': '02341460',    # Chattahoochee River at 14th St, at Columbus, GA [web:23]
    'St. Simons': '03085700',  # Brunswick River at St. Simons, GA (nearby coastal gauge)
    'Macon': '02213000',       # Ocmulgee River at Macon, GA [web:29]
    'Albany': '02359500',      # Flint River at Albany, GA
    'Athens': '02217500',      # Middle Oconee River at Athens, GA
    'Roswell': '02329900'      # Big Creek near Roswell, GA (Chattahoochee tributary)
}

    station_id = GAUGE_STATIONS.get(city_name)
    url = f"https://waterservices.usgs.gov/nwis/iv/?sites={station_id}&parameterCd=00065&format=json"
    
    response = requests.get(url, timeout=5)
    data = response.json()
    
    # Dig into the JSON to get current height
    current_height = float(data['value']['timeSeries'][0]['values'][0]['value'][0]['value'])
    flood_stage = 25.0   # hardcode per city for now
    
    return min(current_height / flood_stage, 1.0)